    //     Ctrl + Shift + | 匹配花括号的闭合处，跳转
    //     Ctrl + ]或[行缩进 （这个功能和tab 和shift tab 的功能相同。）
    //     Ctrl + Shift + [折叠区域代码    Ctrl + Shift + ] 展开区域代码
    //     Ctrl + / 添加关闭行注释
    //     Shift + Alt + A 块区域注释
